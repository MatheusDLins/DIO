produto_1 = 20
produto_2 = 10

print(f"20 + 10 = {produto_1 + produto_2} (Adição)")
print(f"20 - 10 = {produto_1 - produto_2} (Subtração)")
print(f"20 / 10 = {produto_1 / produto_2} (Divisão)")
print(f"20 // 10 = {produto_1 // produto_2} (Divisão inteira)")
print(f"20 * 10 = {produto_1 * produto_2} (Multiplicação)")
print(f"20 % 10 = {produto_1 % produto_2} (Módulo / Resto da divisão)")
print(f"20 ** 10 = {produto_1 ** produto_2} (Exponenciação)")